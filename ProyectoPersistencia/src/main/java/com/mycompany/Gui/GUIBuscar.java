    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Vehiculo veh;
        String PLACA;
        PLACA = txtPlaca.getText();
        veh = ServicioVehiculo.buscarVehiculo(PLACA);
        if ( veh != null)
        {
            int modelo;
            String placa;
            double precio;
            boolean nuevo;
            String estado;

            modelo= veh.getModelo();
            placa= veh.getPlaca();
            precio = veh.getPrecio();
            nuevo = veh.isNuevo();
            estado= veh.getEstado();

            txtModelo.setText(String.valueOf(modelo));
            txtPlaca.setText(placa);
            txtPrecio.setText(String.valueOf(precio));
            boxNuevo.setSelectedItem(nuevo ? "SI" : "NO");
            txtEstado.setText(estado);
        }else{
            JOptionPane.showMessageDialog(this, "NO EXISTE LA PLACA");

        }

    }//GEN-LAST:event_btnBuscarActionPerformed

